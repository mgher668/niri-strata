// Pure Node ESM renderer: generates external theme files from a palette JSON.
// No QML deps. Testable without launching Quickshell.
//
// Input: palette JSON snapshot (the same shape as ThemePresets presets)
//   { dark: { primary: "#9bd4ff", surface: "#101418", ... }, light: { ... } }
//
// Targets:
//   gtk    → GTK CSS file with @define-color directives
//   qt     → KDE color scheme file (.colors format)
//
// Usage from harness:
//   import { renderGtk, renderQt, renderPaletteSnapshot } from "./theme-render.mjs";

// ── palette snapshot ─────────────────────────────────────────────

/**
 * Build a palette JSON snapshot from Theme.colors-style input.
 * Takes an object of camelCase color names → hex values and normalizes
 * it to the full 32-field shape expected by renderers.
 *
 * @param {Object} colors - { primary: "#9bd4ff", surface: "#101418", ... }
 * @returns {Object} normalized palette with all 32 fields
 */
export function snapshotPalette(colors) {
  const REQUIRED = [
    "background", "surface", "layer0", "layer1", "layer1Hover", "layer1Active",
    "surfaceContainerLowest", "surfaceContainerLow", "surfaceContainer",
    "surfaceContainerHigh", "surfaceContainerHighest",
    "text", "mutedText", "subtleText",
    "primary", "primaryText", "primaryContainer", "primaryContainerText",
    "secondary", "secondaryContainer", "secondaryContainerText",
    "tertiary", "tertiaryContainer", "tertiaryContainerText",
    "successColor", "successContainer",
    "warningColor", "warningContainer",
    "errorColor", "errorContainer",
    "outline", "outlineVariant",
  ];

  const out = {};
  for (const field of REQUIRED) {
    out[field] = colors[field] || "#000000";
  }
  return out;
}

