// ── GTK import line for GTK4 ─────────────────────────────────────

/**
 * Generate the @import line that should be injected into gtk-4.0/gtk.css.
 * Uses a marker block so re-runs are idempotent.
 */
export const GTK4_IMPORT_MARKER_START = "/* niri-strata-import-start */";
export const GTK4_IMPORT_MARKER_END = "/* niri-strata-import-end */";

export function gtk4ImportBlock() {
  return GTK4_IMPORT_MARKER_START + "\n" +
    '@import url("niri-strata-colors.css");\n' +
    GTK4_IMPORT_MARKER_END;
}

/**
 * Insert the GTK4 import block into existing gtk.css content.
 * If the marker already exists, replace the old block.
 * If not, prepend the import block at the top.
 * Idempotent: running twice produces the same output.
 */
export function insertGtk4Import(existingContent) {
  const block = gtk4ImportBlock();
  const text = String(existingContent || "");

  // Check for existing marker
  const startIdx = text.indexOf(GTK4_IMPORT_MARKER_START);
  if (startIdx >= 0) {
    const endIdx = text.indexOf(GTK4_IMPORT_MARKER_END);
    if (endIdx >= 0) {
      // Replace existing block
      return text.slice(0, startIdx) + block + text.slice(endIdx + GTK4_IMPORT_MARKER_END.length);
    }
  }

  // Prepend
  return block + "\n" + text;
}